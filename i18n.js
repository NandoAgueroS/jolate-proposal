window.LANG = localStorage.getItem('jolate-lang') || 'es';
document.documentElement.lang = window.LANG;

function applyLang(lang) {
  window.LANG = lang;
  localStorage.setItem('jolate-lang', lang);
  document.documentElement.lang = lang;

  var dict = window.T[lang];
  if (!dict) return;

  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      el.textContent = dict[key];
    }
  });

  if (typeof window.renderDynamicSections === 'function') {
    window.renderDynamicSections();
  }

  var toggle = document.getElementById('lang-toggle');
  if (toggle) toggle.textContent = lang === 'es' ? 'EN' : 'ES';
  var toggleM = document.getElementById('lang-toggle-mobile');
  if (toggleM) toggleM.textContent = lang === 'es' ? 'English' : 'Espanol';
}

applyLang(window.LANG);
